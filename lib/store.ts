import { aiDrafts, leads, notifications, replies } from "@/lib/mockData";
import { getRegion } from "@/lib/regions";
import { scoreLead } from "@/lib/scoring";
import type { AutomationResult, Lead } from "@/lib/types";

let leadStore = [...leads];
let notificationStore = [...notifications];
let replyStore = [...replies];
let aiDraftStore = [...aiDrafts];

export function listLeads(region?: string) {
  return region ? leadStore.filter((lead) => lead.region === region) : leadStore;
}

export function getLead(id: string) {
  return leadStore.find((lead) => lead.id === id);
}

export function listNotifications() {
  return notificationStore;
}

export function listReplies() {
  return replyStore;
}

export function listAiDrafts() {
  return aiDraftStore;
}

