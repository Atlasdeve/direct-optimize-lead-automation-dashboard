export function markOutreach(leadId: string, channel: "email" | "whatsapp") {
  leadStore = leadStore.map((lead) => {
    if (lead.id !== leadId) return lead;
    return {
      ...lead,
      outreach_status: "Contacted",
      email_sent: channel === "email" ? true : lead.email_sent,
      whatsapp_sent: channel === "whatsapp" ? true : lead.whatsapp_sent,
      last_contacted_at: new Date().toISOString(),
      next_follow_up_at: new Date(Date.now() + 3 * 24 * 60 * 60 * 1000).toISOString(),
      updated_at: new Date().toISOString()
    };
  });
}

export function completeAutomation(result: AutomationResult) {
  notificationStore = [
    {
      id: `notification_${Date.now()}`,
      type: result.status === "completed" ? "automation" : "failure",
      title: result.status === "completed" ? "Automation completed" : "Automation failed",
      message: `${result.region}: ${result.leadsFetched} leads, ${result.emailsSent} emails.`,
      created_at: new Date().toISOString(),
      read: false
    },
    ...notificationStore
  ];
}

export function analytics() {
  const days = ["Mon", "Tue", "Wed", "Thu", "Fri", "Sat", "Sun"];
  return {
    daily: days.map((day, index) => ({
      day,
      leads: 8 + index * 3,
      emails: 5 + index * 2,
      whatsapp: 2 + index,
      replies: index % 2 === 0 ? 2 + index : 1 + index
    })),
    regions: ["Canada", "USA", "UK", "UAE", "Qatar"].map((region) => {
      const regionLeads = listLeads(region);
      return {
        region,
        leads: regionLeads.length,
        contacted: regionLeads.filter((lead) => lead.email_sent || lead.whatsapp_sent).length,
        replies: regionLeads.filter((lead) => lead.replied).length
      };
    }),
    funnel: [
      { name: "New Lead", value: leadStore.length },
      { name: "Contacted", value: leadStore.filter((lead) => lead.email_sent || lead.whatsapp_sent).length },
      { name: "Replied", value: leadStore.filter((lead) => lead.replied).length },
      { name: "Meeting Booked", value: leadStore.filter((lead) => lead.outreach_status === "Meeting Booked").length },
      { name: "Client", value: leadStore.filter((lead) => lead.outreach_status === "Closed").length }
    ],
    remaining: {
      emails: 450,
      whatsapp: 120,
      failed: leadStore.filter((lead) => lead.outreach_status === "Failed").length
    }
  };
}
